package seo.dale.algorithm.list.singly;

public class SinglyLinkedListException extends RuntimeException {

	public SinglyLinkedListException() {
		super();
	}

	public SinglyLinkedListException(String message) {
		super(message);
	}

	public SinglyLinkedListException(String message, Throwable cause) {
		super(message, cause);
	}

	public SinglyLinkedListException(Throwable cause) {
		super(cause);
	}
}
