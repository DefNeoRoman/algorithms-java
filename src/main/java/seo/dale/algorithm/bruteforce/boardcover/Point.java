package seo.dale.algorithm.bruteforce.boardcover;

public class Point {

    public int x;
    public int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "Point{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }

    @Override
    protected Point clone() {
        return new Point(x, y);
    }

}
